package miniconverse;

public class MiniPreference {
	private String item;
	private int rating;
	public MiniPreference(String item, int rating) {
		super();
		this.item = item;
		this.rating = rating;
	}
	public String getItem() {
		return item;
	}
	public int getRating() {
		return rating;
	}
	
	
}
