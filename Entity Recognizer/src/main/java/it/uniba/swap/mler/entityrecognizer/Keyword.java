package it.uniba.swap.mler.entityrecognizer;

public class Keyword {
	private String id;
	private String label;
	public Keyword(String id, String label) {
		super();
		this.id = id;
		this.label = label;
	}
	public String getId() {
		return id;
	}
	public String getLabel() {
		return label;
	}
	
}
